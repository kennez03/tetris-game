export function getKicks(type, fromRot, toRot) {
  if (type === 'I') return KICK_I[`${fromRot}>${toRot}`] || [[0, 0]];
  if (type === 'O') return [[0, 0]];
  return KICK_JLSTZ[`${fromRot}>${toRot}`] || [[0, 0]];
}

